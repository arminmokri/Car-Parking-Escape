    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            int vehicle_numbers = Integer.parseInt(jTextFieldVehicleNumbers.getText());
            Map map = new Map(vehicleMatrix, vehicle_numbers);

            Thread thread = new Thread() {
                public void run() {
                    try {
                        Tree tree = new Tree(map);
                        tree.start();
                        ArrayList<Move> moves = tree.getMoves();
                        for (int i = 0; i < moves.size(); i++) {
                            map.moveVehicle(moves.get(i));
                            vehicleMatrix = map.getMatrix();
                            updateGame();
                            Thread.sleep(jSlider1.getValue() * 1000);
                        }
                        JOptionPane.showMessageDialog(null, "Your car escape is ready!");
                    } catch (Exception exception) {
                        exception.printStackTrace();
                        JOptionPane.showMessageDialog(null, exception.getMessage());
                    }
                }
            };
            thread.start();

        } catch (Exception exception) {
            JOptionPane.showMessageDialog(null, exception.toString());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

