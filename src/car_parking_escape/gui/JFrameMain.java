    private void setLocationCenter() {
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        int w = this.getSize().width;
        int h = this.getSize().height;
        int x = (dim.width - w) / 2;
        int y = (dim.height - h) / 2;
        this.setLocation(x, y);
    }

    private Color RandomColor() {
        int r = ThreadLocalRandom.current().nextInt(0, 255);
        int g = ThreadLocalRandom.current().nextInt(0, 255);
        int b = ThreadLocalRandom.current().nextInt(0, 255);
        Color color = new Color(r, g, b);
        return color;
    }

    private void updatejButton2Background() {
        if (jComboBox2.getSelectedIndex() == 0) {
            jButton2.setBackground(Color.WHITE);
        } else {
            int vehicle = Integer.parseInt(jComboBox2.getSelectedItem().toString().split(" ")[1]) - 1;
            jButton2.setBackground(colors[vehicle]);
        }
    }

    private void updateGame() {
        for (int i = 0; i < jPanels.length; i++) {
            for (int j = 0; j < jPanels[0].length; j++) {
                if (vehicleMatrix[i][j] == -1) {
                    jPanels[i][j].setBackground(Color.WHITE);
                    jLabels[i][j].setText("");

                } else {
                    jPanels[i][j].setBackground(colors[vehicleMatrix[i][j]]);
                    jLabels[i][j].setText((vehicleMatrix[i][j] + 1) + "");
                }
            }
        }
        this.repaint();
    }

